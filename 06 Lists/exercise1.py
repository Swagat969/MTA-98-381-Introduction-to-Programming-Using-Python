list1 = [9, 6, 3, 2, 4, 5, 7]

print ("1. ", list1[0])         # 9
print ("2. ", list1[4])         # 4
print ("3. ", list1[-1])        # 7
print ("4. ", list1[-5])        # 3
print ("5. ", list1[:4])        # [9, 6, 3, 2]
print ("6. ", list1[:4:2])      # [9, 3]
print ("7. ", list1[:4:-2])     # [7]
print ("8. ", list1[-1:-5:-2])  # [7, 4]
print ("9. ", list1[::-2])      # [7, 4, 3, 9]
print ("10. ", list1[::3])      # [9, 2, 7]

